
export const ShortForm = (language) => {
    if(language==="Javascript"){
        return "js";
    }
    else if(language==="Python"){
        return "py";
    }
    else if(language==="C++"){
        return "cpp";
    }
    else if(language==="C"){
        return "c";
    }
    else if(language === "Java"){
        return "java";
    }
}